            while not done:
                action = random.randrange(self.n_actions)
                smooth_step = 5
                for i in range(smooth_step):
                    step = env.step(action)
                    if len(step) == 5:
                        obs_next, r, terminated, truncated, _ = step
                        done = terminated or truncated
                        # if terminated:
                        #     print("reward:", r)
                        #     print(step)
                    else:
                        obs_next, r, done, _ = step
                    s = self.discretize_state(obs)
                    s_next = self.discretize_state(obs_next)
                    
                    s_index = self.get_state_index(s)
