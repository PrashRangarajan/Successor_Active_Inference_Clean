    # Discretization helpers
    def create_bins(self):
        pos_space = np.concatenate([
            np.linspace(self.low[0], 0.5, self.n_pos_bins),
            [self.high[0]]
        ]) # Ensure reward consistency for last interval
        vel_space = np.linspace(self.low[1], self.high[1], self.n_vel_bins+1)
        pos_space_regular = np.linspace(self.low[0], self.high[0], self.n_pos_bins+1)
        vel_space_regular = vel_space
        print('Pos Edges', pos_space)
        print('Vel Edges', vel_space)
        return pos_space, vel_space, pos_space_regular, vel_space_regular

    def discretize_state(self, obs):
        pos, vel = obs
        # Clamp for edge cases in bins
        i = clamp(np.digitize(pos, self.pos_space), 1, self.n_pos_bins) - 1
        j = clamp(np.digitize(vel, self.vel_space), 1, self.n_vel_bins) - 1
        return (i, j)

    def get_state_index(self, state):
        return loc_to_idx(state, self.n_vel_bins)

    def get_goal_states(self, goal):
        """
        Returns list of goal states given a goal specification.
        goal: None (default goal at max position), int (position only), or list/array (position, velocity)
        reward: Reward value at goal states
        """
        if goal is None:
            pos = self.n_pos_bins - 1
            vel = None
        elif isinstance(goal, float) or isinstance(goal, int):
            pos = self.discretize_state((goal, 0))[0]
            vel = None
        elif isinstance(goal, (list, np.ndarray)) and len(goal) == 2:
            pos, vel = self.discretize_state(goal)
        else:
            raise ValueError("Invalid goal specification")
        if vel is not None:
            goal_states = [self.get_state_index((pos, vel))]
        else:
            goal_states = [self.get_state_index((pos, j)) for j in range(self.n_vel_bins)]
        return goal_states
    
    def get_observation(self, state_index):
        return idx_to_loc(state_index, self.n_vel_bins)

